	public Document generateReport(List<Map<String, Object>> responseList, ReportsMappingModel model,
			File responseFile,String inputJson) {
		
		
		HeaderFooterPageEvent event =new HeaderFooterPageEvent(model);
		 Document document = new Document(PageSize.A4, 36, 36, 150, 36);

		try {
			PdfWriter writer = PdfWriter.getInstance(document, new FileOutputStream(responseFile));
			writer.setPageEvent(event); 
			document.open();
			
			Map<String, List<Map<String, Object>>> salesByProductDetails = responseList.stream()
					.collect(Collectors.groupingBy(map -> (String) map.get("CUSTOMER_NM")));			
			
			
			if(!ObjectUtils.isEmpty(salesByProductDetails)) { 
				
				for(String customerName :salesByProductDetails.keySet()) {					
					List<Map<String, Object>> salesProductDetails = salesByProductDetails.get(customerName);
					createTable(document,model,salesProductDetails,customerName);
				}
				
				
			}
			

		} catch (Exception e) {
			//log.error(ExceptionUtils.getMessage(e));
			try {
				addMessage(document, ExceptionUtils.getMessage(e));
			} catch (DocumentException e1) {
				//log.error(ExceptionUtils.getMessage(e1));
			}
		} finally {
			document.close();
		}
		
		return document;
	}


